package com.itacademy.six.loop;

/**
 * varijabla apetlkje -> number
 *
 * uslov petlje
 * blok koda petlje
 * korak petlje
 */
public class ForDemo {
    public static void main(String[] args) {
        for(int number= 11; number >0; number--) {
            System.out.println(number);
        }
    }
}
